package BitManipulation;

public class BitManipulationTest {
    public static void main(String[] args) {

        int value = 20;
        System.out.println("Value "+value);

        int result = 20 << 1;
        System.out.println(result);

        result = value << 2;
        System.out.println(result);

        result = value << 3;
        System.out.println(result);

        result = value << 4;
        System.out.println(result);

    }


}
